package encapsulation;

public class SampleEncap {
	private String mailId;
	private int accNo;
	private int accBalc;
	public String getMailId() {
		return mailId;
	}
	public void setMailId(String mailId) {
		this.mailId = mailId;
	}
	public int getAccNo() {
		return accNo;
	}
	public void setAccNo(int accNo) {
		this.accNo = accNo;
	}
	public int getAccBalc() {
		return accBalc;
	}
	public void setAccBalc(int accBalc) {
		this.accBalc = accBalc;
	}
	

}
